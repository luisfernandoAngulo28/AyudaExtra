    public void mostrarPila(){
        int k=P.GetCima();
        int v[]=new int [k];
        PilaBit A=P;
        for (int i=0;i<=k;i++){
            v[i]=A.PopBit();
        }
        for (int j=0;j<=v.length;j++){
            list1.add(" "+v[j]);
        }
    }
    
