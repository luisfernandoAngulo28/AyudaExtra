    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            Object data1[][] = new Object[getNroFil()][getNroCol()];
            Object ColumnName1[] = new Object[getNroCol()];
            for (int i = 0; i < getNroCol(); i++) {
                ColumnName1[i] = i + 1;
            }
            DefaultTableModel dftm1 = new DefaultTableModel(data1, ColumnName1);
            jTable2.setModel(dftm1);
            jTable2.setEnabled(true);
            for (int i = 0; i < getNroFil(); i++) {
                for (int j = 0; j < getNroCol(); j++) {
                    jTable2.setValueAt(sp.GetDato(i + 1, j + 1), i, j);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error::Mostrar Datos");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

