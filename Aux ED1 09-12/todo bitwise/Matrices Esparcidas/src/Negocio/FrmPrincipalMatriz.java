    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            /*setNroFil(Integer.parseInt(jTextField1.getText().trim()));
            setNroCol(Integer.parseInt(jTextField2.getText().trim()));*/
            setNroFil(Integer.parseInt(JOptionPane.showInputDialog("Nro de Filas:")));
            setNroCol(Integer.parseInt(JOptionPane.showInputDialog("Nro de Columnas:")));
            int elem = Integer.parseInt(JOptionPane.showInputDialog("Elemento Predominante:"));
            Object[][] data = new Object[getNroFil()][getNroCol()];
            Object[] columnNames = new Object[getNroCol()];
            for (int i = 0; i < getNroCol(); i++) {
                columnNames[i] = i + 1;
            }
            DefaultTableModel dftm = new DefaultTableModel(data, columnNames);
            jTable1.setModel(dftm);
            jTable1.setEnabled(true);
            sp = new Sparce(getNroFil(), getNroCol(), elem);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Datos Incorrectos:: Verifique");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            for (int i = 0; i < getNroFil(); i++) {
                for (int j = 0; j < getNroCol(); j++) {
                    if (jTable1.getValueAt(i, j) != null) {
                        float dato = Float.parseFloat((String) jTable1.getValueAt(i, j));
                        sp.SetDato(i + 1, j + 1, dato);
                    }
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

